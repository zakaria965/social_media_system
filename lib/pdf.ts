      doc.setFont("helvetica", "bold")
      doc.setFontSize(9)
      doc.text("G", 20.2, 23)

      const [darkR, darkG, darkB] = hexToRgb("#0f172a")
      doc.setTextColor(darkR, darkG, darkB)
      doc.setFont("helvetica", "bold")
      doc.setFontSize(10)
      doc.text("GrowWave", 28, 23)

      const [grayR, grayG, grayB] = hexToRgb("#64748b")
      doc.setTextColor(grayR, grayG, grayB)
      doc.setFont("helvetica", "normal")
      doc.setFontSize(6)
      doc.text("Performance Intelligence Report", 54, 22)
      doc.text("www.growwave.com", 192, 22, { align: "right" })

      const [lineR, lineG, lineB] = hexToRgb("#e2e8f0")
      doc.setDrawColor(lineR, lineG, lineB)
      doc.setLineWidth(0.3)
      doc.line(18, 28, 192, 28)

      // Report Title Meta
      doc.setTextColor(darkR, darkG, darkB)
      doc.setFont("helvetica", "bold")
      doc.setFontSize(14)
      doc.text("GrowWave Analytics Report", 18, 36)
      doc.setTextColor(grayR, grayG, grayB)
      doc.setFont("helvetica", "normal")
      doc.setFontSize(7)
      doc.text("Performance Intelligence Summary", 18, 41)

      // Details Block
      const timeframeMap: Record<string, string> = {
        "7d": "Last 7 Days",
        "30d": "Last 30 Days",
        "90d": "Last 90 Days",
        "12m": "Last 12 Months",
      }
      const timeRangeStr = timeframeMap[payload.timeframe] || payload.timeframe

      const [bgR, bgG, bgB] = hexToRgb("#f8fafc")
      doc.setFillColor(bgR, bgG, bgB)
      doc.roundedRect(18, 46, 174, 13, 2, 2, "F")

      const [subR, subG, subB] = hexToRgb("#475569")
      doc.setTextColor(subR, subG, subB)
      doc.setFont("helvetica", "bold")
      doc.setFontSize(5.5)
      doc.text("WORKSPACE", 23, 51)
      doc.text("GENERATED DATE", 78, 51)
      doc.text("TIME RANGE", 140, 51)

      doc.setTextColor(darkR, darkG, darkB)
      doc.setFont("helvetica", "normal")
      doc.setFontSize(6.5)
      doc.text(payload.workspaceName, 23, 56)
      doc.text(payload.generatedDate, 78, 56)
      doc.text(timeRangeStr, 140, 56)

      // Executive AI Summary Box
      doc.setFillColor(bgR, bgG, bgB)
      doc.rect(18, 63, 174, 24, "F")
      doc.setDrawColor(lineR, lineG, lineB)
      doc.setLineWidth(0.3)
      doc.roundedRect(18, 63, 174, 24, 2, 2, "S")

      doc.setTextColor(tealR, tealG, tealB)
      doc.setFont("helvetica", "bold")
      doc.setFontSize(5.5)
      doc.text("EXECUTIVE AI SUMMARY", 23, 69)

      const summaryText = payload.aiIntelligence && payload.aiIntelligence.length > 0
